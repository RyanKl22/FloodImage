public class Pixel {

    private int x;
    private int y;
    private int colorBity;

    public Pixel(int x, int y, int colorBity) {
        this.x = x;
        this.y = y;
        this.colorBity =colorBity;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getColorBity() {
        return colorBity;
    }

}
